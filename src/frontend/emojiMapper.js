// Create reverse mapping (word to emoji)
export const wordToEmoji = Object.entries(emojiToWord).reduce(
  (acc, [emoji, word]) => {
    acc[word.toLowerCase()] = emoji;
    return acc;
  },
  {}
);

// Translate text to emojis
export const translateToEmojis = (text) => {
  if (!text.trim()) return '';
  
  const words = text.toLowerCase().split(/\s+/);
  return words
    .map(word => {
      // Remove punctuation for matching
      const cleanWord = word.replace(/[.,!?;:]$/, '');
      const punctuation = word.match(/[.,!?;:]$/) || [''];
      
      return wordToEmoji[cleanWord] 
        ? wordToEmoji[cleanWord] + punctuation[0]
        : word;
    })
    .join(' ');
};

